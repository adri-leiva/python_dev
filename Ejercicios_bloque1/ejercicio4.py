num_a= int(input (f"ingrese el valor de a: "))
num_b= int(input (f"ingrese el valor de b: "))

print (f"{num_a} + {num_b} = {num_a+num_b}")
print (f"{num_a} - {num_b} = {num_a-num_b}")
print (f"{num_a} * {num_b} = {num_a*num_b}")
print (f"{num_a} / {num_b} = {num_a/num_b}")
